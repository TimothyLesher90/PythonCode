fruits = ["apple", "banana", "cherry"]
print(fruits)

# Accessing elements
print(fruits[0])  # First element
print(fruits[-1]) # Last element

# Adding elements
fruits.append("orange")
print(fruits)

# Removing elements
fruits.remove("banana")
print(fruits)

#For loop
for fruit in fruits:
    print (fruit)
